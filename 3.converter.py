#rupee to dollar converter

rupee = float(input("Enter the amount of rupees: "))
dollar = round(rupee/87.19,3)
print(f"the equivalent {dollar} for {rupee} is", dollar)

#temperature convertor

celcius = float(input("Enter degrees: "))
farenheit= float((1.8*celcius)+32)
print(f"the equivalent *F to {celcius} is",farenheit)


